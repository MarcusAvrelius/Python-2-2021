import turtle as tr

for nums in [[(30, 30), (30, -30)], [(60,30), (60, 0), (90, 0), (90, 30), (90, -30)], [(120,0), (150,30), (150, -30)], [(180,30), (210, 30), (180, 0), (180, -30)], [(240, 30), (270, 30), (270, -30), (240, -30), (240, 30)], [(300, 30), (330, 30), (330, -30), (300, -30), (300, 30)]]:
    for cords in nums:
             tr.goto(cords)
             tr.pendown()
    tr.penup()
    
